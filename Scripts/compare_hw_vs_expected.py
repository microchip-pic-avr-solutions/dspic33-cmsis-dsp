def to_signed(v):
    if v >= 0x80000000:
        return v - 0x100000000
    return v

LOG2N = 7
def bitrev(i, bits):
    j = 0
    for b in range(bits):
        if i & (1 << b):
            j |= (1 << (bits - 1 - b))
    return j

matches = 0
mismatches = 0
match_indices = []
mismatch_details = []

for i in range(256):
    h = to_signed(hw[i])
    e = to_signed(exp[i])
    if h == e:
        matches += 1
        match_indices.append(i)
    else:
        mismatches += 1
        diff = h - e
        mismatch_details.append((i, hw[i], exp[i], diff))

print(f"Total: {matches} matches, {mismatches} mismatches")

# Analyze: which complex indices are fully correct?
correct_complex = set()
for i in range(128):
    if 2*i in match_indices and 2*i+1 in match_indices:
        correct_complex.add(i)
print(f"\nFully correct complex indices ({len(correct_complex)}):", sorted(correct_complex))

# Check if hw could be the non-bit-reversed DIF output
print("\nChecking if hw is non-bit-reversed version of expected...")
no_br_matches = 0
for i in range(256):
    cplx = i // 2
    part = i % 2
    br_cplx = bitrev(cplx, LOG2N)
    br_idx = br_cplx * 2 + part
    h = to_signed(hw[br_idx])
    e = to_signed(exp[i])
    if h == e:
        no_br_matches += 1
print(f"Matches with swapped bit-reversal: {no_br_matches}/256")

# Check if hw output is DOUBLE the expected (wrong scaling)
print("\nChecking if hw = 2 * expected (missing one stage of scaling)...")
double_matches = 0
for i in range(256):
    h = to_signed(hw[i])
    e = to_signed(exp[i]) * 2
    if h == e:
        double_matches += 1
print(f"Matches with hw = 2*expected: {double_matches}/256")

# Check if hw = expected without 1/2 at last stage  
print("\nChecking if hw has different DIF computation (not rounding)...")
# Look at ratio hw/expected for mismatches
print("\nFirst 30 mismatches with ratio:")
for idx, h, e, d in mismatch_details[:30]:
    cplx = idx // 2
    part = 'Re' if idx % 2 == 0 else 'Im'
    hs = to_signed(h)
    es = to_signed(e)
    ratio = hs / es if es != 0 else float('inf')
    print(f"  [{idx:3d}] (cplx {cplx:3d} {part}) hw=0x{h:08X} exp=0x{e:08X} diff={d:10d} ratio={ratio:.4f}")

